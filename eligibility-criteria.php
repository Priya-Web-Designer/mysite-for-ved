            <h2 class="text-center fw-bold text-primary">UPSC Civil Services Exam – Eligibility Criteria</h2>
            <p>The Union Public Service Commission (UPSC) outlines a structured eligibility framework for the Civil Services Examination (CSE), including services like the IAS, IPS, IFS, IRS, and more. Understanding this framework is critical before embarking on your preparation journey. Vedanta IAS Academy ensures every student begins their UPSC journey on a strong, compliant footing.</p>

            <hr>

            <h3 class="py-3 text-danger"><b>NATIONALITY – Who Can Apply?</b></h3>
            <p>For Indian Administrative Service (IAS), Indian Police Service (IPS), and Indian Foreign Service (IFS):</p>
            <p><b>Must be a citizen of India</b></p>
            <p>For other services (e.g., IRS, IRTS, IAAS, etc.):</p>
            <p><b>Must be either:</b></p>
            <ul>
                <li>A citizen of India,</li>
                <li>A subject of Nepal or Bhutan,</li>
                <li>A Tibetan refugee who came to India before January 1, 1962, with the intention of permanently settling in India,</li>
                <li>A person of Indian origin (PIO) who migrated from specified countries (Pakistan, Myanmar, Sri Lanka, Kenya, Uganda, Tanzania, Zambia, Malawi, Ethiopia, or Vietnam) intending permanent settlement in India</li>
            </ul>
            <p><b>Note:</b></p>
            <ul>
                <li>Non-Indian citizens must obtain a certificate of eligibility from the Government of India (usually issued by the Ministry of Home Affairs).</li>
                <li>Such a certificate is not required at the time of Preliminary Exam application, but must be produced before final selection.</li>
            </ul>

            <hr>

            <h3 class="py-3 text-danger"><b>EDUCATIONAL QUALIFICATIONS – Minimum Academic Requirement</b></h3>
            <p>A candidate must possess:</p>
            <p><b> A graduate degree (Bachelor’s) in any discipline from a recognized university</b></p>
            <p>Recognized Institutions Include:</p>
            <ul>
                <li>Central, State, and Deemed Universities</li>
                <li>Distance education universities (IGNOU, Symbiosis, etc.)</li>
                <li>Private universities recognized by UGC</li>
                <li>Foreign degrees recognized by the Association of Indian Universities (AIU)</li>
            </ul>
            <p><b>Final-Year Students:</b></p>
            <ul>
                <li>Eligible to appear in the Preliminary Examination</li>
                <li>Must provide proof of passing the final exam before appearing in the Main Examination</li>
            </ul>
            <p>Technical and Professional Qualifications:</p>
            <ul><li>Degrees like B.Tech, MBBS, CA, CS, ICWA are all considered valid</li></ul>
            <p>Special Cases:</p>
            <ul><li>Candidates awaiting results but declared qualified by their university are also eligible to apply</li></ul>

            <hr>

            <h3 class="py-3 text-danger"><b> AGE LIMIT – Minimum & Maximum Age (As of August 1 of the Exam Year)</b></h3>
            <p><b>Minimum Age: 21 years</b></p>
            <p><b>Maximum Age:</b></p>
            <ul>
                <li>General: 32 years</li>
                <li>OBC (Non-Creamy Layer): 35 years</li>
                <li>SC/ST: 37 years</li>
                <li>
                    PwBD:
                    <ul>
                        <li>General: 42 years</li>
                        <li>OBC: 45 years</li>
                        <li>SC/ST: 47 years</li>
                    </ul>
                </li>
                <li>Ex-servicemen: 5-year relaxation for those disabled in duty (as per category rules)</li>
            </ul>
            <p><b>Example for UPSC CSE 2025:</b></p>
            <ul><li>A General category aspirant must be born between August 2, 1993, and August 1, 2004</li></ul>

            <hr>

            <h3 class="py-3 text-danger"><b>NUMBER OF ATTEMPTS – How Many Chances Do You Get?</b></h3>
            <ul>
                <li>General: 6 attempts up to 32 years</li>
                <li>OBC: 9 attempts up to 35 years</li>
                <li>SC/ST: Unlimited attempts (till age limit)</li>
                <li>
                    PwBD:
                    <ul>
                        <li>General & OBC: 9 attempts</li>
                        <li>SC/ST PwBD: Unlimited</li>
                    </ul>
                </li>
            </ul>
            <p><b>Attempt Definition:</b></p>
            <ul>
                <li>Simply appearing in the Prelims exam counts as an attempt</li>
                <li>Filling the form but skipping the exam does NOT count as an attempt</li>
            </ul>

            <hr>

            <h3 class="py-3 text-danger"><b>RESERVATION & RELAXATION – Social & Economic Categories</b></h3>
            <p><b>Reservation in age, attempts, and seats is available for:</b></p>
            <ul>
                <li>SC/ST/OBC (with valid caste certificate)</li>
                <li>EWS (Economically Weaker Sections – based on income/assets criteria)</li>
                <li>PwBD (benchmark disabilities as notified by UPSC)</li>
            </ul>
            <p><b>Required Certificates:</b></p>
            <ul>
                <li>Must be issued by a competent authority (Tehsildar/DM/SDM)</li>
                <li>Must comply with the latest DoPT guidelines</li>
            </ul>
            <p>Vedanta’s counseling cell helps students obtain and verify valid certificates to avoid rejection at the DAF/interview stage.</p>

            <hr>

            <h3 class="py-3 text-danger"><b>MEDICAL STANDARDS – Fitness Criteria (Post-Mains Selection)</b></h3>
            <p><b>For services like IAS/IFS/IPS, candidates must meet prescribed medical standards:</b></p>
            <ul>
                <li>Vision, hearing, BMI, and locomotor capabilities are assessed</li>
                <li>IPS and certain armed services require stricter fitness parameters</li>
                <li>Disabilities are assessed as per RPWD Act (2016)</li>
            </ul>
            <p>Vedanta arranges orientation on medical boards and common disqualifications after Mains for better preparedness.</p>

            <hr>

            <h3 class="py-3 text-danger"><b>EXAM DEBARMENT – Who Is Ineligible?</b></h3>
            <p><b>The following candidates are barred from taking the UPSC CSE:</b></p>
            <ul>
                <li>Candidates debarred by UPSC due to misconduct or cheating</li>
                <li>Candidates who have exhausted their attempts or crossed the age limit</li>
                <li>Candidates who conceal facts or submit fake documents</li>
            </ul>
            <p>Vedanta conducts verification checks during registration to ensure candidate eligibility before course enrollment.</p>

