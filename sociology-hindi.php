      <h2 class="text-center py-4 text-danger"><b>पेपर - I</b></h2>
      <div class="col-lg-8 col-md-8 col-12 text-justify">
        <h3 class="text-center pb-2 text-primary">समाजशास्त्र के मूल सिद्धांत</h3>
        <p>
          <b>1. समाजशास्त्र – एक अनुशासन</b><br>
          (a) आधुनिकता और यूरोप में सामाजिक परिवर्तन तथा समाजशास्त्र का उदय।<br>
          (b) विषय की प्रकृति और अन्य सामाजिक विज्ञानों से तुलना।<br>
          (c) समाजशास्त्र और सामान्य ज्ञान।
        </p>
        <p>
          <b>2. एक विज्ञान के रूप में समाजशास्त्र</b><br>
          (a) विज्ञान, वैज्ञानिक विधि और उसकी आलोचना।<br>
          (b) शोध पद्धति की प्रमुख सैद्धांतिक धाराएँ।<br>
          (c) पोजिटिविज्म और उसकी आलोचना।<br>
          (d) तथ्य, मूल्य और वस्तुनिष्ठता।<br>
          (e) गैर-पोजिटिविस्ट कार्यप्रणालियाँ।
        </p>
        <p>
          <b>3. अनुसंधान विधियाँ और विश्लेषण</b><br>
          (a) गुणात्मक और मात्रात्मक विधियाँ।<br>
          (b) आंकड़े संग्रहण की तकनीकें।<br>
          (c) चर, सैम्पलिंग, परिकल्पना, विश्वसनीयता और वैधता।
        </p>
        <p>
          <b>4. प्रमुख समाजशास्त्री</b><br>
          (a) कार्ल मार्क्स – ऐतिहासिक भौतिकवाद, उत्पादन के साधन, विमूर्तिकरण, वर्ग संघर्ष।<br>
          (b) एमिल दुर्खीम – श्रम विभाजन, सामाजिक तथ्य, आत्महत्या, धर्म और समाज।<br>
          (c) मैक्स वेबर – सामाजिक क्रिया, आदर्श प्रकार, अधिकार, नौकरशाही, प्रोटेस्टेंट एथिक और पूंजीवाद की आत्मा।<br>
          (d) टाल्कॉट पार्सन्स – सामाजिक प्रणाली, पैटर्न वैरिएबल।<br>
          (e) रॉबर्ट के. मर्टन – प्रकट और अंतर्निहित क्रियाएँ, अनुकूलन और विचलन, संदर्भ समूह।<br>
          (f) मीड – आत्म और पहचान।
        </p>
        <p>
          <b>5. सामाजिक स्तरीकरण और गतिशीलता</b><br>
          (a) अवधारणाएँ – समानता, असमानता, पदानुक्रम, बहिष्करण, गरीबी और वंचना।<br>
          (b) सामाजिक स्तरीकरण के सिद्धांत – संरचनात्मक क्रियात्मकतावादी, मार्क्सवादी, वेबरीय सिद्धांत।<br>
          (c) आयाम – वर्ग, पद समूह, लिंग, जातीयता और नस्ल के आधार पर सामाजिक स्तरीकरण।<br>
          (d) सामाजिक गतिशीलता – खुली और बंद प्रणालियाँ, गतिशीलता के प्रकार, स्रोत और कारण।
        </p>
        <p>
          <b>6. कार्य और आर्थिक जीवन</b><br>
          (a) विभिन्न प्रकार के समाजों में कार्य की सामाजिक संरचना – दास समाज, सामंती समाज, औद्योगिक / पूंजीवादी
          समाज।<br>
          (b) कार्य की औपचारिक और अनौपचारिक संरचना।<br>
          (c) श्रम और समाज।
        </p>
        <p>
          <b>7. राजनीति और समाज</b><br>
          (a) सत्ता के समाजशास्त्रीय सिद्धांत।<br>
          (b) पावर एलीट, नौकरशाही, दबाव समूह और राजनीतिक दल।<br>
          (c) राष्ट्र, राज्य, नागरिकता, लोकतंत्र, नागरिक समाज, विचारधारा।<br>
          (d) विरोध, आंदोलन, सामाजिक आंदोलन, सामूहिक क्रिया, क्रांति।
        </p>
        <p>
          <b>8. धर्म और समाज</b><br>
          (a) धर्म के समाजशास्त्रीय सिद्धांत।<br>
          (b) धार्मिक प्रथाओं के प्रकार – एनीमिज्म, मोनिज्म, बहुलवाद, संप्रदाय, पंथ।<br>
          (c) आधुनिक समाज में धर्म – धर्म और विज्ञान, धर्मनिरपेक्षता, धार्मिक पुनरुत्थान, कट्टरपंथ।
        </p>
        <p>
          <b>9. संबंधों की प्रणाली</b><br>
          (a) परिवार, गृहस्थी, विवाह।<br>
          (b) परिवार के प्रकार और स्वरूप।<br>
          (c) वंश और वंशावली।<br>
          (d) पितृसत्ता और श्रम का लैंगिक विभाजन।<br>
          (e) समकालीन प्रवृत्तियाँ।
        </p>
        <p>
          <b>10. आधुनिक समाज में सामाजिक परिवर्तन</b><br>
          (a) सामाजिक परिवर्तन के समाजशास्त्रीय सिद्धांत।<br>
          (b) विकास और आश्रयवाद।<br>
          (c) सामाजिक परिवर्तन के कारक।<br>
          (d) शिक्षा और सामाजिक परिवर्तन।<br>
          (e) विज्ञान, प्रौद्योगिकी और सामाजिक परिवर्तन।
        </p>

        <h2 class="text-center py-4 text-danger"><b>द्वितीय प्रश्नपत्र</b></h2>
        <h3 class="text-center py-2 text-primary">भारतीय समाज : संरचना और परिवर्तन</h3>

        <p>
          <b>क. भारतीय समाज का परिचय</b><br>
          (i) भारतीय समाज के अध्ययन के दृष्टिकोण<br>
          (a) इंडोलॉजी (जी.एस. घुर्ये)।<br>
          (b) संरचनात्मक क्रियात्मक दृष्टिकोण (एम.एन. श्रीनिवास)।<br>
          (c) मार्क्सवादी समाजशास्त्र (ए.आर. देसाई)।<br>

          (ii) उपनिवेशकालीन शासन का भारतीय समाज पर प्रभाव<br>
          (a) भारतीय राष्ट्रवाद की सामाजिक पृष्ठभूमि।<br>
          (b) भारतीय परंपरा का आधुनिकीकरण।<br>
          (c) औपनिवेशिक काल में विरोध और आंदोलन।<br>
          (d) सामाजिक सुधार आंदोलन।
        </p>

        <p>
          <b>ख. सामाजिक संरचना</b><br>
          (i) ग्रामीण और कृषक सामाजिक संरचना<br>
          (a) भारतीय गाँव की अवधारणा और ग्राम अध्ययन।<br>
          (b) भूमि स्वामित्व प्रणाली का विकास, भूमि सुधार।<br>

          (ii) जाति व्यवस्था<br>
          (a) जाति प्रणाली के अध्ययन के दृष्टिकोण – घुर्ये, श्रीनिवास, लुई ड्यूमोंट, आंद्रे बेतेले।<br>
          (b) जाति व्यवस्था की विशेषताएँ।<br>
          (c) अस्पृश्यता – रूप और दृष्टिकोण।<br>

          (iii) भारत में जनजातियाँ<br>
          (a) परिभाषात्मक समस्याएँ।<br>
          (b) भौगोलिक विस्तार।<br>
          (c) औपनिवेशिक नीतियाँ और जनजातियाँ।<br>
          (d) एकीकरण और स्वायत्तता के मुद्दे।<br>

          (iv) भारत में सामाजिक वर्ग<br>
          (a) कृषक वर्ग संरचना।<br>
          (b) औद्योगिक वर्ग संरचना।<br>
          (c) भारत में मध्यवर्ग।<br>

          (v) भारत में संबंधों की प्रणाली<br>
          (a) भारत में वंश और वंशावली।<br>
          (b) संबंध प्रणाली के प्रकार।<br>
          (c) भारत में परिवार और विवाह।<br>
          (d) परिवार के घरेलू आयाम।<br>
          (e) पितृसत्ता, अधिकार और श्रम का लैंगिक विभाजन।<br>

          (vi) धर्म और समाज<br>
          (a) भारत में धार्मिक समुदाय।<br>
          (b) धार्मिक अल्पसंख्यकों की समस्याएँ।
        </p>

        <p>
          <b>ग. भारत में सामाजिक परिवर्तन</b><br>
          (i) भारत में सामाजिक परिवर्तन की कल्पनाएँ<br>
          (a) विकास योजना और मिश्रित अर्थव्यवस्था की अवधारणा।<br>
          (b) संविधान, कानून और सामाजिक परिवर्तन।<br>
          (c) शिक्षा और सामाजिक परिवर्तन।<br>

          (ii) भारत में ग्रामीण और कृषि परिवर्तन<br>
          (a) ग्रामीण विकास कार्यक्रम, सामुदायिक विकास योजना, सहकारिता, गरीबी उन्मूलन योजनाएँ।<br>
          (b) हरित क्रांति और सामाजिक परिवर्तन।<br>
          (c) कृषि उत्पादन के बदलते रूप।<br>
          (d) ग्रामीण मजदूरों की समस्याएँ, बंधुआ मजदूरी, प्रवास।<br>

          (iii) भारत में औद्योगीकरण और शहरीकरण<br>
          (a) भारत में आधुनिक उद्योग का विकास।<br>
          (b) भारत में शहरी बस्तियों का विकास।<br>
          (c) श्रमिक वर्ग – संरचना, विकास, वर्गीय आंदोलन।<br>
          (d) अनौपचारिक क्षेत्र, बाल श्रम।<br>
          (e) शहरी क्षेत्रों में झुग्गियाँ और वंचना।<br>

          (iv) राजनीति और समाज<br>
          (a) राष्ट्र, लोकतंत्र और नागरिकता।<br>
          (b) राजनीतिक दल, दबाव समूह, सामाजिक और राजनीतिक अभिजात वर्ग।<br>
          (c) क्षेत्रीयता और सत्ता का विकेंद्रीकरण।<br>
          (d) धर्मनिरपेक्षता।<br>

          (v) आधुनिक भारत में सामाजिक आंदोलन<br>
          (a) किसान और कृषक आंदोलन।<br>
          (b) महिला आंदोलन।<br>
          (c) पिछड़ा वर्ग और दलित आंदोलन।<br>
          (d) पर्यावरणीय आंदोलन।<br>
          (e) जातीयता और पहचान आंदोलन।<br>

          (vi) जनसंख्या गतिकी<br>
          (a) जनसंख्या का आकार, वृद्धि, संरचना और वितरण।<br>
          (b) जनसंख्या वृद्धि के घटक – जन्म, मृत्यु, प्रवास।<br>
          (c) जनसंख्या नीति और परिवार नियोजन।<br>
          (d) उभरते मुद्दे – वृद्धावस्था, लिंगानुपात, शिशु मृत्यु दर, प्रजनन स्वास्थ्य।<br>

          (vii) सामाजिक परिवर्तन की चुनौतियाँ<br>
          (a) विकास संकट – विस्थापन, पर्यावरणीय समस्याएँ और स्थिरता।<br>
          (b) गरीबी, वंचना और असमानताएँ।<br>
          (c) महिलाओं के विरुद्ध हिंसा।<br>
          (d) जातिगत संघर्ष।<br>
          (e) जातीय संघर्ष, सांप्रदायिकता, धार्मिक पुनरुत्थान।<br>
          (f) अशिक्षा और शैक्षिक असमानताएँ।<br>
        </p>
