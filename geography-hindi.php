        <div class="row position-relative px-lg-5 px-md-3-px-1 pb-5">
            <h2 class="text-center py-3 text-danger"><b>पेपर-I</b></h2>
            <div class="col-lg-8 col-md-8 col-12 text-justify">
                <h3 class="text-center py-2 text-primary"><b>भौतिक भूगोल के सिद्धांत:</b></h3>

                <p>
                    1- <b>भूमिरूपविज्ञान</b>: भू-आकृति विकास को नियंत्रित करने वाले कारक; अंतर्जन्य और बहिर्जन्य बल;
                    पृथ्वी की सतह की उत्पत्ति और विकास; भू-चुंबकत्व के मूल सिद्धांत; पृथ्वी के आंतरिक भाग की भौतिक
                    स्थिति; भू-सिंकुचन; महाद्वीपीय विस्थापन; समस्थिति; प्लेट विवर्तनिकी; पर्वत निर्माण पर नवीन
                    दृष्टिकोण; ज्वालामुखीयता; भूकंप और सूनामी; भू-आकृतिक चक्रों और भू-दृश्य विकास की अवधारणाएँ; अपरदन
                    कालक्रम; जलधारा की आकृति; अपरदन सतहें; ढलान विकास; अनुप्रयुक्त भूमिरूपविज्ञान: भूजलविज्ञान, आर्थिक
                    भूविज्ञान और पर्यावरण।
                </p>
                <p>
                    2- <b>जलवायु विज्ञान</b>: विश्व की तापमान और दबाव पट्टियाँ; पृथ्वी का ऊष्मा संतुलन; वायुमंडलीय
                    संचलन; वायुमंडलीय स्थिरता और अस्थिरता; ग्रहों और स्थानीय पवनें; मानसून और जेट स्ट्रीम; वायुमंडलीय
                    संघटन और मोर्चा निर्माण; शीतोष्ण और उष्णकटिबंधीय चक्रवात; वर्षा के प्रकार और वितरण; मौसम और जलवायु;
                    कोपेन, थॉर्नथवेट और ट्रेवार्था की विश्व जलवायु वर्गीकरण; जल चक्र; वैश्विक जलवायु परिवर्तन और मानव की
                    भूमिका व प्रतिक्रिया; अनुप्रयुक्त जलवायु विज्ञान और शहरी जलवायु।
                </p>
                <p>
                    3- <b>महासागर विज्ञान</b>: अटलांटिक, हिंद और प्रशांत महासागरों की तलरचना; महासागरों का तापमान और
                    लवणता; ऊष्मा और लवण संतुलन; महासागरीय निक्षेप; लहरें, धाराएँ और ज्वार; समुद्री संसाधन: जैविक, खनिज
                    और ऊर्जा संसाधन; मूंगा भित्तियाँ, मूंगा विरंजन; समुद्र तल परिवर्तन; समुद्री कानून और समुद्री
                    प्रदूषण।
                </p>
                <p>
                    4- <b>जैव भूगोल</b>: मृदा की उत्पत्ति; मृदा का वर्गीकरण और वितरण; मृदा प्रोफाइल; मृदा अपरदन, क्षरण
                    और संरक्षण; पौधों और जानवरों के वैश्विक वितरण को प्रभावित करने वाले कारक; वनों की कटाई की समस्याएँ
                    और संरक्षण उपाय; सामाजिक वानिकी; कृषि वानिकी; वन्यजीव; प्रमुख जीन पूल केंद्र।
                </p>
                <p>
                    5- <b>पर्यावरणीय भूगोल</b>: पारिस्थितिकी के सिद्धांत; मानव की पारिस्थितिक अनुकूलन; पर्यावरण पर मानव
                    का प्रभाव; वैश्विक और क्षेत्रीय पारिस्थितिक परिवर्तन और असंतुलन; पारिस्थितिकी तंत्र का प्रबंधन और
                    संरक्षण; पर्यावरणीय क्षरण, प्रबंधन और संरक्षण; जैव विविधता और सतत विकास; पर्यावरण नीति; पर्यावरणीय
                    खतरे और उपचारात्मक उपाय; पर्यावरण शिक्षा और विधि।
                </p>

                <h3 class="text-center py-4 text-primary"><b>मानव भूगोल</b></h3>

                <p>
                    1- <b>मानव भूगोल में दृष्टिकोण</b>: क्षेत्रीय विभेदीकरण; क्षेत्रीय संश्लेषण; द्वैतवाद; पर्यावरणवाद;
                    मात्रात्मक क्रांति और स्थिति विश्लेषण; रेडिकल, व्यवहारवादी, मानवतावादी और कल्याणकारी दृष्टिकोण;
                    भाषाएँ, धर्म और धर्मनिरपेक्षता; विश्व के सांस्कृतिक क्षेत्र; मानव विकास सूचकांक।
                </p>
                <p>
                    2- <b>आर्थिक भूगोल</b>: विश्व आर्थिक विकास: मापन और समस्याएँ; विश्व संसाधन और उनका वितरण; ऊर्जा
                    संकट; विकास की सीमाएँ; विश्व कृषि: कृषि क्षेत्रों का प्रकार्य वर्गीकरण; कृषि इनपुट और उत्पादकता;
                    खाद्य और पोषण समस्याएँ; खाद्य सुरक्षा; अकाल: कारण, प्रभाव और समाधान; विश्व उद्योग: स्थानिक प्रतिरूप
                    और समस्याएँ; व्यापार प्रतिरूप।
                </p>
                <p>
                    3- <b>जनसंख्या और निवास भूगोल</b>: विश्व जनसंख्या की वृद्धि और वितरण; जनसांख्यिकीय विशेषताएँ; प्रवास
                    के कारण और परिणाम; अधि-अल्प और इष्टतम जनसंख्या की अवधारणाएँ; जनसंख्या सिद्धांत, विश्व जनसंख्या
                    समस्याएँ और नीतियाँ; सामाजिक कल्याण और जीवन गुणवत्ता; जनसंख्या को सामाजिक पूंजी के रूप में देखना;
                    ग्रामीण बस्तियों के प्रकार और प्रतिरूप; ग्रामीण बस्तियों के पर्यावरणीय मुद्दे; शहरी बस्तियों का
                    पदानुक्रम; शहरी आकारिकी: प्राइमेट शहर और रैंक-आकार नियम; नगरों का कार्यात्मक वर्गीकरण; शहरी प्रभाव
                    क्षेत्र; ग्रामीण-शहरी सीमा क्षेत्र; उपग्रह नगर; शहरीकरण की समस्याएँ और समाधान; नगरों का सतत विकास।
                </p>
                <p>
                    4- <b>क्षेत्रीय नियोजन</b>: क्षेत्र की अवधारणा; क्षेत्रों के प्रकार और क्षेत्रीयकरण की विधियाँ;
                    विकास केंद्र और विकास ध्रुव; क्षेत्रीय असंतुलन; क्षेत्रीय नियोजन में पर्यावरणीय मुद्दे; सतत विकास के
                    लिए नियोजन।
                </p>
                <p>
                    5- <b>मानव भूगोल में मॉडल, सिद्धांत और नियम</b>: मानव भूगोल में प्रणाली विश्लेषण; माल्थसियन,
                    मार्क्सवादी और जनसंख्या संक्रमण मॉडल; क्रिस्टलर और लॉश के केंद्रीय स्थान सिद्धांत; वॉन थ्यूनन का
                    कृषि स्थान मॉडल; वेबर का औद्योगिक स्थान मॉडल; रोस्टो का विकास चरण मॉडल; हार्टलैंड और रिमलैंड
                    सिद्धांत; अंतर्राष्ट्रीय सीमाओं और सीमांत क्षेत्रों के नियम।
                </p>

                <h2 class="text-center py-3 text-danger"><b>पेपर-II</b></h2>
                <h3 class="text-center py-2 text-primary"><b>भारत का भूगोल</b></h3>

                <p>
                    1- <b>भौतिक संरचना:</b> भारत की पड़ोसी देशों से स्थानिक संबद्धता; संरचना और स्थलाकृति; जल निकासी
                    तंत्र और जलग्रहण क्षेत्र; भौगोलिक क्षेत्र; भारतीय मानसून और वर्षा प्रणाली की कार्यप्रणाली,
                    उष्णकटिबंधीय चक्रवात और पश्चिमी विक्षोभ; बाढ़ और सूखा; जलवायु क्षेत्र; प्राकृतिक वनस्पति; मृदा
                    प्रकार और उनका वितरण।
                </p>
                <p>
                    2-<b>संसाधन:</b> भूमि, सतही और भूजल, ऊर्जा, खनिज, जैविक और समुद्री संसाधन; वन और वन्य जीव संसाधन और
                    उनका संरक्षण; ऊर्जा संकट।
                </p>
                <p>
                    3-<b>कृषि: अधोसंरचना:</b> सिंचाई, बीज, उर्वरक, विद्युत; संस्थागत कारक: भूमि जोत, भूमि अधिकार और भूमि
                    सुधार; फसल प्रतिरूप, कृषि उत्पादकता, कृषि तीव्रता, फसल संयोजन, भूमि क्षमता; कृषि वानिकी और सामाजिक
                    वानिकी; हरित क्रांति और उसके सामाजिक-आर्थिक व पारिस्थितिक प्रभाव; शुष्क खेती का महत्व; पशु संसाधन और
                    श्वेत क्रांति; मत्स्य पालन, रेशम उत्पादन, मधुमक्खी पालन और कुक्कुट पालन; कृषि क्षेत्रीयकरण;
                    कृषि-जलवायु क्षेत्र; कृषि-पारिस्थितिक क्षेत्र।
                </p>
                <p>
                    4-<b>उद्योग:</b> उद्योगों का विकास; कपड़ा, जूट, इस्पात, एल्युमिनियम, उर्वरक, कागज, रसायन और औषधि,
                    ऑटोमोबाइल, कुटीर और कृषि आधारित उद्योगों के स्थानिक कारक; औद्योगिक घराने और परिसर, सार्वजनिक क्षेत्र
                    की इकाइयाँ; औद्योगिक क्षेत्रीयकरण; नवीन औद्योगिक नीतियाँ; बहुराष्ट्रीय कंपनियाँ और उदारीकरण; विशेष
                    आर्थिक क्षेत्र; पर्यटन और पारिस्थितिक पर्यटन।
                </p>
                <p>
                    5-<b>परिवहन, संचार और व्यापार:</b> सड़क, रेल, जलमार्ग, हवाई और पाइपलाइन नेटवर्क और क्षेत्रीय विकास
                    में उनकी पूरक भूमिकाएँ; राष्ट्रीय और विदेशी व्यापार में बंदरगाहों का महत्व; व्यापार संतुलन; व्यापार
                    नीति; निर्यात प्रसंस्करण क्षेत्र; संचार और सूचना प्रौद्योगिकी का विकास और उनका अर्थव्यवस्था व समाज
                    पर प्रभाव; भारतीय अंतरिक्ष कार्यक्रम।
                </p>
                <p>
                    6-<b>सांस्कृतिक संरचना:</b> भारतीय समाज का ऐतिहासिक परिप्रेक्ष्य; नस्लीय, भाषाई और जातीय विविधता;
                    धार्मिक अल्पसंख्यक; प्रमुख जनजातियाँ, जनजातीय क्षेत्र और उनकी समस्याएँ; सांस्कृतिक क्षेत्र; जनसंख्या
                    की वृद्धि, वितरण और घनत्व; जनसांख्यिकीय विशेषताएँ: लिंगानुपात, आयु संरचना, साक्षरता दर, कार्यबल,
                    आश्रित अनुपात, जीवन प्रत्याशा; प्रवासन (अंतर-क्षेत्रीय, अंतः-क्षेत्रीय और अंतर्राष्ट्रीय) और संबंधित
                    समस्याएँ; जनसंख्या समस्याएँ और नीतियाँ; स्वास्थ्य संकेतक।
                </p>
                <p>
                    7-<b>निवास:</b> ग्रामीण बस्तियों के प्रकार, प्रतिरूप और संरचना; शहरी विकास; भारतीय शहरों की संरचना;
                    भारतीय शहरों का कार्यात्मक वर्गीकरण; महानगरीय क्षेत्र और नगर समूह; शहरी फैलाव; झुग्गियाँ और संबंधित
                    समस्याएँ; नगर नियोजन; शहरीकरण की समस्याएँ और समाधान।
                </p>
                <p>
                    8-<b>क्षेत्रीय विकास और नियोजन:</b> भारत में क्षेत्रीय नियोजन का अनुभव; पंचवर्षीय योजनाएँ; एकीकृत
                    ग्रामीण विकास कार्यक्रम; पंचायती राज और विकेंद्रीकृत नियोजन; कमांड क्षेत्र विकास; जलग्रहण प्रबंधन;
                    पिछड़े क्षेत्र, मरुस्थल, सूखा प्रभावित, पर्वतीय और जनजातीय क्षेत्र विकास की योजना; बहु-स्तरीय
                    नियोजन; द्वीप क्षेत्रों का नियोजन।
                </p>
                <p>
                    9-<b>राजनीतिक पहलू:</b> भारतीय संघवाद का भौगोलिक आधार; राज्यों का पुनर्गठन; नए राज्यों का उद्भव;
                    क्षेत्रीय चेतना और अंतर-राज्यीय मुद्दे; भारत की अंतरराष्ट्रीय सीमाएँ और संबंधित समस्याएँ; सीमा पार
                    आतंकवाद; विश्व मामलों में भारत की भूमिका; दक्षिण एशिया और हिंद महासागर क्षेत्र की भू-राजनीति।
                </p>
                <p>
                    10-<b>समकालीन मुद्दे:</b> पारिस्थितिक मुद्दे: पर्यावरणीय खतरे: भूस्खलन, भूकंप, सूनामी, बाढ़ और सूखा,
                    महामारी; पर्यावरण प्रदूषण से संबंधित मुद्दे; भूमि उपयोग प्रतिरूपों में परिवर्तन; पर्यावरणीय प्रभाव
